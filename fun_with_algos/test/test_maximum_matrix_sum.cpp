#include "maximum_matrix_sum.h"
#include "gtest/gtest.h"

TEST(TestMaximumMatrixSum, simple1)
{
    std::vector<std::vector<int>> matrix{{1, -1}, {-1, 1}};
    constexpr long long expected_result{4};
    EXPECT_EQ(maximum_matrix_sum(matrix), expected_result);
}


TEST(TestMaximumMatrixSum, simple2)
{
    std::vector<std::vector<int>> matrix{{1, 2, 3}, {-1, -2, -3}, {1, 2, 3}};
    constexpr long long expected_result{16};
    EXPECT_EQ(maximum_matrix_sum(matrix), expected_result);
}

